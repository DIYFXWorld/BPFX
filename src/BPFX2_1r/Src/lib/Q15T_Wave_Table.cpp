Q15T Q15T_Get_Wave_Table_Data( uint16_t idx, const int16_t* const Table )
{
	Q15T	result;

	int	decimal_0 = idx & 0xff;
	int	decimal_1 = 256 - decimal_0;

	int	index_0 = idx >> 8;
	int	index_1 = ( index_0 == 255 ) ? 0 : index_0 + 1;

	result.Value =	Table[ index_0 ] * decimal_1 / 256 +
									Table[ index_1 ] * decimal_0 / 256;
	return result;
}

